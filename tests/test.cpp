#include <iostream>

using namespace std;

// Function to add two numbers
int add(int a, int b)
{
    return a + b;
}

// Main function
int main()
{
    // Print Hello, World!
    cout << "Hello, World!" << endl;

    // Print the result of adding 1 and 2
    cout << "1 + 2 = " << add(1, 2) << endl;

    // Return 0 to indicate successful completion
    return 0;
}
